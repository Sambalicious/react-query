import Head from "next/head";
import {
  useAddUser,
  useDeleteUser,
  useGetUser,
  useGetUsers,
  useUpdateUser,
} from "../hooks";
import styles from "../styles/Home.module.css";

export default function Home() {
  // const { data } = useGetPosts();

  const { data: users } = useGetUsers(); /** get a list */
  const mutationAddUser = useAddUser((oldUsers, newUser) => [
    newUser,
    ...oldUsers,
  ]);
  const deleteMutation = useDeleteUser((oldUsers, id) =>
    oldUsers.filter(user => user.id !== id),
  );

  const userUpdateMutation = useUpdateUser(1);
  /** add a new data */
  const onAdd = async () => {
    try {
      await mutationAddUser.mutateAsync({
        name: "Test",
        id: 1,
      });
    } catch (error) {}
  };

  /** delete a user */
  const deleteUser = async (id: number) => {
    await deleteMutation.mutateAsync(id);
  };

  /** update a single user */
  const updateUser = (id: number) => {
    userUpdateMutation.mutate({
      name: "Test",
      id,
    });
  };

  /** fetch a single data */
  const { data: user } = useGetUser(1);

  return (
    <div className={styles.container}>
      <Head>
        <title>Everything React query </title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <div>
          <ul>
            {users?.map(el => (
              <li className="my-2" key={user?.id}>
                <div className="flex justify-between">
                  <p>{el.name} </p>
                  <div>
                    <button onClick={() => deleteUser(el?.id)}>Delete </button>
                    <button onClick={() => updateUser(el.id)}>Edit</button>
                  </div>
                </div>
              </li>
            ))}
          </ul>
        </div>
        <button style={{ marginBlock: "2rem" }} onClick={onAdd}>
          Add new user{" "}
        </button>
      </main>
    </div>
  );
}
